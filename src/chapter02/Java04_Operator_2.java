package chapter02;

public class Java04_Operator_2 {
    public static void main(String[] args) {
        /*
        TODO 运算符 --- 关系运算符：==,!=,>,>=,<,<=
         比较两个数据之间的关系
         基本语法结构：
          关系表达式：元素 1  关系运算符  元素 2
          结果为布尔类型；相同为 true 不同为 false

         */
        int i = 10;
        int j = 20;
        System.out.println(i > j);
        System.out.println(i < j);
        System.out.println(i == j);//是否相等
        System.out.println(i != j);//是否不相等

    }
}
